      <h2>Tampodruk</h2>
      <div class="title-red-line"></div>
      <p>Technika druku polegająca na nakładaniu farby drukarskiej za pomocą miękkiego silikonowego stempla zwanego tamponem. Za pomocą tamponu o odpowiednim kształcie wykonywany jest nadruk na nierównych i nieregularnych powierzchniach.</p>
      <p>Przez dobranie odpowiedniej farby możliwe jest drukowanie na podłożach takich jak tworzywa sztuczne, guma, szkło, metal itp.</p>
      <p>Posiadamy maszyny jedno-, dwu- i czterokolorowe – dysponując takim parkiem maszynowym jesteśmy w stanie wykonać najbardziej skomplikowane formy znakowania.</p>
