      <h2>Tłoczenie</h2>
      <div class="title-red-line"></div>
      <p>Proces tworzenia w materiale tłoczonym trójwymiarowego wzoru poprzez ściśnięcie matrycą zazwyczaj w celu uwypuklenia elementu.</p>
      <p>Tłoczenie odbywa się w temperaturze otoczenia lub tłocznik jest podgrzewany do temperatury kilkudziesięciu °C, dzięki czemu tłoczony materiał po ustąpieniu tłoka w mniejszym stopniu zmienia strukturę. Czasem używane są wyższe temperatury, by tłoczony materiał dodatkowo przebarwić (np. skórę). Dysponujemy maszyną do hotprintu o pow. znakowania do 1500 cm. kw.</p>
