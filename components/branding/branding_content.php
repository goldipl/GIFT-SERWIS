        <h2>Sitodruk</h2>
        <div class="title-red-line"></div>
        <p>Formę drukową stanowi prostokątna rama, zwykle aluminiowa z napiętą na niej siatką z szablonem. Szablon tworzy utwardzona warstwa światłoczuła, nie przepuszczająca farby, stanowiąca negatywowy obraz drukowanego wzoru.</p>
