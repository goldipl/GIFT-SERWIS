      <h2>Sitodruk</h2>
      <div class="title-red-line"></div>
      <p>Formę drukową stanowi prostokątna rama, zwykle aluminiowa z napiętą na niej siatką z szablonem. Szablon tworzy utwardzona warstwa światłoczuła, nie przepuszczająca farby, stanowiąca negatywowy obraz drukowanego wzoru.</p>
      <p>Podczas drukowania w maszynie płaskiej, farba drukowa jest  rozprowadzana na całej powierzchni siatki i przesuwającym się po niej raklem jest przeciskana przez wolne oczka siatki bezpośrednio na podłoże drukowe. Posiadamy stoły sitodrukowe w formatach od B2 do A0, sześciokolorową karuzelę czteropozycyjną, zespoły przesuszające oraz prasy termiczne.</p>
