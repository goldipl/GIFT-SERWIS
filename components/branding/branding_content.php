      <h2>Kalka ceramiczna</h2>
      <div class="title-red-line"></div>
      <p>Technika nanoszenia wzoru odbywa się poprzez wypalanie kalki ceramicznej w piecu w temperaturze ok. 800°C, co pozwala uzyskać bardzo dużą trwałość wykonanego zdobienia.</p>
      <p>Tak naniesiony wydruk odporny jest na  ścieranie, podgrzewanie w kuchenkach mikrofalowych, mycie mechaniczne i działanie chemicznych środków czyszczących. Dekoracje wykonane techniką wypału kalki ceramicznej dają długotrwałą wytrzymałość (do 1500 cykli) na mycie w zmywarkach. Dysponujemy komorowymi piecami firmy RHODE.</p>
